/// <summary>
/// Generates interceptor source code for Quarry method call sites.
/// </summary>
/// <remarks>
/// Interceptors use the [InterceptsLocation] attribute to redirect method calls
/// to generated code that provides compile-time SQL generation and typed reader delegates.
/// All interceptors use the 'file' scope modifier to prevent visibility to end users.
/// </remarks>
internal static class InterceptorCodeGenerator
{
    /// <summary>
    /// Generates the interceptors file for a context.
    /// </summary>
    public static string GenerateInterceptorsFile(
        string contextClassName,
        string? contextNamespace,
        IReadOnlyList<UsageSiteInfo> usageSites,
        IReadOnlyList<PrebuiltChainInfo>? prebuiltChains = null)
    {
        var sb = new StringBuilder();

        // File header
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();

        // Required usings
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Data.Common;");
        sb.AppendLine("using System.Linq.Expressions;");
        sb.AppendLine("using System.Reflection;");
        sb.AppendLine("using System.Runtime.CompilerServices;");
        sb.AppendLine("using System.Threading;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using System.Diagnostics.CodeAnalysis;");
        sb.AppendLine("using Quarry;");
        sb.AppendLine("using Quarry.Internal;");

        // Collect all unique entity type namespaces
        var entityNamespaces = usageSites
            .Select(s => GetNamespaceFromTypeName(s.EntityTypeName))
            .Where(ns => !string.IsNullOrEmpty(ns) && ns != "Quarry" && ns != "System")
            .Distinct()
            .ToList();

        // Also collect namespaces from the source file of each usage site
        // This helps when entity types are from generated files in the same generator run
        var fileNamespaces = usageSites
            .Select(s => GetNamespaceFromFilePath(s.InvocationSyntax))
            .Where(ns => !string.IsNullOrEmpty(ns) && ns != "Quarry" && ns != "System")
            .Distinct();

        // Collect context namespaces — generated entity types live in the context's namespace
        var contextNamespaces = usageSites
            .Select(s => s.ContextNamespace)
            .Where(ns => !string.IsNullOrEmpty(ns) && ns != "Quarry" && ns != "System")
            .Distinct();

        var allNamespaces = entityNamespaces
            .Concat(fileNamespaces)
            .Concat(contextNamespaces!)
            .Distinct()
            .OrderBy(ns => ns);

        foreach (var ns in allNamespaces)
        {
            sb.AppendLine($"using {ns};");
        }
        sb.AppendLine();

        // InterceptsLocation attribute definition
        // Uses new format with (int version, string data)
        GenerateInterceptsLocationAttribute(sb);
        sb.AppendLine();

        // Namespace - interceptors must not be in global namespace
        // Use block namespace (not file-scoped) since we already have a block namespace for the attribute
        var interceptorNamespace = !string.IsNullOrEmpty(contextNamespace)
            ? contextNamespace
            : "Quarry.Generated";
        sb.AppendLine($"namespace {interceptorNamespace}");
        sb.AppendLine("{");
        sb.AppendLine();

        // File-scoped interceptor class
        sb.AppendLine($"/// <summary>");
        sb.AppendLine($"/// Generated interceptors for {contextClassName} query methods.");
        sb.AppendLine($"/// </summary>");
        sb.AppendLine($"file static class {contextClassName}Interceptors");
        sb.AppendLine("{");

        // Build chain analysis lookups for execution interceptor generation
        var chainLookup = new Dictionary<string, PrebuiltChainInfo>();
        var clauseBitMap = new Dictionary<string, int>(); // site UniqueId → bit index
        var chainMemberIds = new HashSet<string>(); // all site UniqueIds that are part of analyzed chains
        if (prebuiltChains != null)
        {
            foreach (var chain in prebuiltChains)
            {
                chainLookup[chain.Analysis.ExecutionSite.UniqueId] = chain;
                chainMemberIds.Add(chain.Analysis.ExecutionSite.UniqueId);
                foreach (var clause in chain.Analysis.Clauses)
                {
                    chainMemberIds.Add(clause.Site.UniqueId);
                    if (clause.IsConditional && clause.BitIndex.HasValue)
                    {
                        clauseBitMap[clause.Site.UniqueId] = clause.BitIndex.Value;
                    }
                }
            }
        }

        // Build clause → chain lookup for simplified clause interceptors in prebuilt chains
        var chainClauseLookup = new Dictionary<string, PrebuiltChainInfo>();
        var firstClauseIds = new HashSet<string>();
        if (prebuiltChains != null)
        {
            foreach (var chain in prebuiltChains)
            {
                foreach (var clause in chain.Analysis.Clauses)
                {
                    chainClauseLookup[clause.Site.UniqueId] = chain;
                }
                if (chain.Analysis.Clauses.Count > 0)
                {
                    firstClauseIds.Add(chain.Analysis.Clauses[0].Site.UniqueId);
                }
            }
        }

        // Collect all static fields needed for cached extractors
        var staticFields = CollectStaticFields(usageSites, chainMemberIds);

        // Collect all unique TypeMapping classes used across all usage sites
        var mappingInstances = CollectMappingInstances(usageSites, chainMemberIds);

        // Collect all unique EntityReader classes used across all usage sites
        var entityReaderInstances = CollectEntityReaderInstances(usageSites, chainMemberIds);

        if (staticFields.Count > 0 || mappingInstances.Count > 0 || entityReaderInstances.Count > 0)
        {
            sb.AppendLine("    #region Cached Fields");
            sb.AppendLine();
            foreach (var field in staticFields)
            {
                sb.AppendLine($"    private static FieldInfo? {field.FieldName};");
            }
            foreach (var mapping in mappingInstances)
            {
                sb.AppendLine($"    private static readonly {mapping.Value} {mapping.Key} = new();");
            }
            foreach (var reader in entityReaderInstances)
            {
                sb.AppendLine($"    private static readonly {reader.Value} {reader.Key} = new();");
            }
            sb.AppendLine();
            sb.AppendLine("    #endregion");
            sb.AppendLine();
        }

        // Generate interceptor methods grouped by kind.
        // Include non-analyzable sites that are part of analyzed chains (conditional clause sites).
        var groupedSites = usageSites
            .Where(s => s.IsAnalyzable || chainMemberIds.Contains(s.UniqueId))
            .GroupBy(s => s.Kind)
            .OrderBy(g => g.Key);

        foreach (var group in groupedSites)
        {
            sb.AppendLine($"    #region {group.Key} Interceptors");
            sb.AppendLine();

            foreach (var site in group)
            {
                GenerateInterceptorMethod(sb, site, staticFields, chainLookup, clauseBitMap, chainClauseLookup, firstClauseIds);
            }

            sb.AppendLine($"    #endregion");
            sb.AppendLine();
        }

        sb.AppendLine("}"); // Close interceptor class
        sb.AppendLine("}"); // Close namespace

        return sb.ToString();
    }

    /// <summary>
    /// Represents a static field for caching a FieldInfo used for parameter extraction.
    /// </summary>
    private sealed class CachedExtractorField
    {
        public CachedExtractorField(
            string fieldName,
            string methodName,
            int parameterIndex,
            string expressionPath)
        {
            FieldName = fieldName;
            MethodName = methodName;
            ParameterIndex = parameterIndex;
            ExpressionPath = expressionPath;
        }

        public string FieldName { get; }
        public string MethodName { get; }
        public int ParameterIndex { get; }
        /// <summary>Raw dot-separated path like "Body.Right" or "Body.Arguments[0]".</summary>
        public string ExpressionPath { get; }
    }

    /// <summary>
    /// Collects all static fields needed for cached extractors across all usage sites.
    /// </summary>
    private static List<CachedExtractorField> CollectStaticFields(IReadOnlyList<UsageSiteInfo> usageSites, HashSet<string> chainMemberIds)
    {
        var fields = new List<CachedExtractorField>();

        foreach (var site in usageSites.Where(s => s.IsAnalyzable || chainMemberIds.Contains(s.UniqueId)))
        {
            var methodName = $"{site.MethodName}_{site.UniqueId}";

            // Skip sites that won't generate captured parameter extraction
            if (site.Kind == InterceptorKind.Select && ShouldSkipSelectInterceptor(site))
                continue;

            var clauseInfo = site.ClauseInfo;
            if (clauseInfo == null || !clauseInfo.IsSuccess)
                continue;

            var capturedParams = clauseInfo.Parameters
                .Where(p => p.IsCaptured && p.CanGenerateDirectPath)
                .ToList();
            foreach (var param in capturedParams)
            {
                var fieldName = $"_{methodName}_p{param.Index}";
                fields.Add(new CachedExtractorField(
                    fieldName,
                    methodName,
                    param.Index,
                    param.ExpressionPath!));
            }
        }

        return fields;
    }

    /// <summary>
    /// Collects all unique TypeMapping class FQNs used across all usage sites and returns
    /// a mapping from field name to FQN for generating cached static readonly instances.
    /// </summary>
    private static Dictionary<string, string> CollectMappingInstances(IReadOnlyList<UsageSiteInfo> usageSites, HashSet<string> chainMemberIds)
    {
        var mappings = new Dictionary<string, string>(); // fieldName → FQN

        foreach (var site in usageSites.Where(s => s.IsAnalyzable || chainMemberIds.Contains(s.UniqueId)))
        {
            // From insert columns
            if (site.InsertInfo != null)
            {
                foreach (var col in site.InsertInfo.Columns)
                {
                    if (col.CustomTypeMappingClass != null)
                        AddIfMissing(mappings, GetMappingFieldName(col.CustomTypeMappingClass), col.CustomTypeMappingClass);
                }
            }

            // From projection columns
            if (site.ProjectionInfo != null)
            {
                foreach (var col in site.ProjectionInfo.Columns)
                {
                    if (col.CustomTypeMapping != null)
                        AddIfMissing(mappings, GetMappingFieldName(col.CustomTypeMapping), col.CustomTypeMapping);
                }
            }

            // From where clause parameters
            if (site.ClauseInfo != null)
            {
                foreach (var p in site.ClauseInfo.Parameters)
                {
                    if (p.CustomTypeMappingClass != null)
                        AddIfMissing(mappings, GetMappingFieldName(p.CustomTypeMappingClass), p.CustomTypeMappingClass);
                }

                // From Set clause mapping
                if (site.ClauseInfo is SetClauseInfo setInfo && setInfo.CustomTypeMappingClass != null)
                    AddIfMissing(mappings, GetMappingFieldName(setInfo.CustomTypeMappingClass), setInfo.CustomTypeMappingClass);
            }
        }

        return mappings;
    }

    /// <summary>
    /// Gets a safe field name for a TypeMapping class instance cache.
    /// E.g. "MyApp.MoneyMapping" → "_mapper_MyApp_MoneyMapping"
    /// </summary>
    private static void AddIfMissing(Dictionary<string, string> dict, string key, string value)
    {
        if (!dict.ContainsKey(key))
            dict[key] = value;
    }

    /// <summary>
    /// Collects all unique EntityReader class FQNs used across all usage sites.
    /// </summary>
    private static Dictionary<string, string> CollectEntityReaderInstances(IReadOnlyList<UsageSiteInfo> usageSites, HashSet<string> chainMemberIds)
    {
        var readers = new Dictionary<string, string>(); // fieldName → FQN

        foreach (var site in usageSites.Where(s => s.IsAnalyzable || chainMemberIds.Contains(s.UniqueId)))
        {
            if (site.ProjectionInfo?.CustomEntityReaderClass != null)
            {
                var fqn = site.ProjectionInfo.CustomEntityReaderClass;
                AddIfMissing(readers, GetEntityReaderFieldName(fqn), fqn);
            }
        }

        return readers;
    }

    /// <summary>
    /// Gets a safe field name for an EntityReader class instance cache.
    /// E.g. "MyApp.UserReader" → "_entityReader_MyApp_UserReader"
    /// </summary>
    internal static string GetEntityReaderFieldName(string readerClassFqn)
    {
        return "_entityReader_" + readerClassFqn.Replace('.', '_').Replace('+', '_');
    }

    internal static string GetMappingFieldName(string mappingClassFqn)
    {
        return "_mapper_" + mappingClassFqn.Replace('.', '_').Replace('+', '_');
    }

    /// <summary>
    /// Wraps a parameter value expression with ToDb() if the parameter has a custom type mapping.
    /// </summary>
    private static string WrapWithToDb(string valueExpr, ParameterInfo param)
    {
        if (param.CustomTypeMappingClass != null)
            return $"{GetMappingFieldName(param.CustomTypeMappingClass)}.ToDb({valueExpr})";
        return valueExpr;
    }

    /// <summary>
    /// Generates the InterceptsLocationAttribute if needed.
    /// Uses new format: InterceptsLocationAttribute(int version, string data)
    /// </summary>
    private static void GenerateInterceptsLocationAttribute(StringBuilder sb)
    {
        sb.AppendLine("namespace System.Runtime.CompilerServices");
        sb.AppendLine("{");
        sb.AppendLine("    [AttributeUsage(AttributeTargets.Method, AllowMultiple = true)]");
        sb.AppendLine("    file sealed class InterceptsLocationAttribute : Attribute");
        sb.AppendLine("    {");
        sb.AppendLine("        public InterceptsLocationAttribute(int version, string data) { }");
        sb.AppendLine("    }");
        sb.AppendLine("}");
    }

    /// <summary>
    /// Extracts the namespace from a syntax node's containing file.
    /// </summary>
    private static string? GetNamespaceFromFilePath(Microsoft.CodeAnalysis.SyntaxNode? syntaxNode)
    {
        if (syntaxNode?.SyntaxTree == null)
            return null;

        var root = syntaxNode.SyntaxTree.GetRoot();

        // Look for file-scoped namespace
        var fileScopedNamespace = root.DescendantNodes()
            .OfType<Microsoft.CodeAnalysis.CSharp.Syntax.FileScopedNamespaceDeclarationSyntax>()
            .FirstOrDefault();
        if (fileScopedNamespace != null)
        {
            return fileScopedNamespace.Name.ToString();
        }

        // Look for block-scoped namespace containing the invocation
        var namespaceDecl = syntaxNode.Ancestors()
            .OfType<Microsoft.CodeAnalysis.CSharp.Syntax.NamespaceDeclarationSyntax>()
            .FirstOrDefault();
        if (namespaceDecl != null)
        {
            return namespaceDecl.Name.ToString();
        }

        // Fallback: look for any namespace in the file
        var anyNamespace = root.DescendantNodes()
            .OfType<Microsoft.CodeAnalysis.CSharp.Syntax.NamespaceDeclarationSyntax>()
            .FirstOrDefault();
        if (anyNamespace != null)
        {
            return anyNamespace.Name.ToString();
        }

        return null;
    }

    /// <summary>
    /// Converts an absolute file path to a shorter relative-style path for display in comments.
    /// Returns a path starting from a recognized project marker, or the last few path segments.
    /// </summary>
    private static string GetRelativePath(string filePath)
    {
        if (string.IsNullOrEmpty(filePath))
            return filePath;

        // Common project root markers to look for
        string[] markers = ["src", "test", "tests", "lib", "samples", "examples", "benchmark", "benchmarks"];

        foreach (var marker in markers)
        {
            // Try with backslash (Windows)
            var index = filePath.IndexOf(marker + "\\", StringComparison.OrdinalIgnoreCase);
            if (index >= 0)
                return filePath.Substring(index);

            // Try with forward slash (Unix/normalized)
            index = filePath.IndexOf(marker + "/", StringComparison.OrdinalIgnoreCase);
            if (index >= 0)
                return filePath.Substring(index);
        }

        // No marker found - return last 3 path segments for context
        var segments = filePath.Split(Path.DirectorySeparatorChar, Path.AltDirectorySeparatorChar);
        if (segments.Length <= 3)
            return filePath;

        var lastSegments = new string[3];
        Array.Copy(segments, segments.Length - 3, lastSegments, 0, 3);
        return string.Join(Path.DirectorySeparatorChar.ToString(), lastSegments);
    }

    /// <summary>
    /// Extracts the namespace from a fully qualified type name.
    /// </summary>
    private static string? GetNamespaceFromTypeName(string typeName)
    {
        // Handle global:: prefix
        if (typeName.StartsWith("global::"))
        {
            typeName = typeName.Substring(8);
        }

        // Find the last dot that's not within angle brackets (generic types)
        int depth = 0;
        int lastDotIndex = -1;

        for (int i = typeName.Length - 1; i >= 0; i--)
        {
            char c = typeName[i];
            if (c == '>')
                depth++;
            else if (c == '<')
                depth--;
            else if (c == '.' && depth == 0)
            {
                lastDotIndex = i;
                break;
            }
        }

        if (lastDotIndex <= 0)
            return null;

        return typeName.Substring(0, lastDotIndex);
    }

    /// <summary>
    /// Checks if a clause interceptor should be skipped because the clause could not be translated.
    /// When skipped, the original runtime method runs instead of a silent no-op fallback.
    /// </summary>
    private static bool ShouldSkipNonTranslatableClause(UsageSiteInfo site)
    {
        switch (site.Kind)
        {
            case InterceptorKind.Where:
            case InterceptorKind.DeleteWhere:
            case InterceptorKind.UpdateWhere:
                return site.ClauseInfo == null || !site.ClauseInfo.IsSuccess;

            case InterceptorKind.OrderBy:
            case InterceptorKind.ThenBy:
                if (site.ClauseInfo is OrderByClauseInfo orderByInfo && orderByInfo.IsSuccess)
                    return false;
                return site.ClauseInfo == null || !site.ClauseInfo.IsSuccess;

            case InterceptorKind.GroupBy:
            case InterceptorKind.Having:
                return site.ClauseInfo == null || !site.ClauseInfo.IsSuccess;

            case InterceptorKind.Set:
            case InterceptorKind.UpdateSet:
                if (site.ClauseInfo is SetClauseInfo setInfo && setInfo.IsSuccess)
                    return false;
                return site.ClauseInfo == null || !site.ClauseInfo.IsSuccess;

            case InterceptorKind.UpdateSetPoco:
                return site.UpdateInfo == null || site.UpdateInfo.Columns.Count == 0;

            case InterceptorKind.InsertExecuteNonQuery:
            case InterceptorKind.InsertExecuteScalar:
                return site.InsertInfo == null || site.InsertInfo.Columns.Count == 0;

            default:
                return false;
        }
    }

    /// <summary>
    /// Checks if a Select interceptor should be skipped entirely (not emitted).
    /// </summary>
    private static bool ShouldSkipSelectInterceptor(UsageSiteInfo site)
    {
        var projection = site.ProjectionInfo;
        if (projection == null)
            return false; // Let the fallback path handle it

        // Skip anonymous types - they should have been rejected by QRY014
        if (projection.Kind == ProjectionKind.Anonymous)
            return true;

        // Skip unknown or failed projections
        if (projection.Kind == ProjectionKind.Unknown)
            return true;

        // Skip non-optimal paths
        if (!projection.IsOptimalPath)
            return true;

        // Skip tuple projections with invalid result types
        // This can happen when the entity types are generated by the same source generator
        // and the semantic model can't fully resolve the tuple element types
        if (projection.Kind == ProjectionKind.Tuple)
        {
            var resultType = projection.ResultTypeName;
            if (string.IsNullOrWhiteSpace(resultType) ||
                !resultType.StartsWith("(") ||
                resultType.Contains("( ") ||
                resultType.Contains(", )") ||
                ContainsUnresolvedTupleElement(resultType))
            {
                return true;
            }
        }

        return false;
    }

    /// <summary>
    /// Checks if a tuple type string contains unresolved elements (missing types).
    /// </summary>
    private static bool ContainsUnresolvedTupleElement(string tupleType)
    {
        // Remove parentheses
        var inner = tupleType.Substring(1, tupleType.Length - 2);
        var parts = inner.Split(',');

        foreach (var part in parts)
        {
            var trimmed = part.Trim();
            if (string.IsNullOrEmpty(trimmed))
                return true;

            // Check if this looks like just a name without a type
            // Valid: "int UserId", "string? Name"
            // Invalid: "UserId", "Name"
            var spaceIdx = trimmed.IndexOf(' ');
            if (spaceIdx < 0)
            {
                // Single word - must be a type like "int" or "DateTime"
                // If it starts with uppercase and isn't a known type, it's likely just a name
                if (trimmed.Length > 0 && char.IsUpper(trimmed[0]) && !IsKnownUppercaseTypeName(trimmed))
                {
                    return true;
                }
            }
        }

        return false;
    }

    /// <summary>
    /// Checks if a name is a known .NET type that starts with uppercase.
    /// </summary>
    private static bool IsKnownUppercaseTypeName(string name)
    {
        var baseName = name.TrimEnd('?');
        return baseName switch
        {
            "Boolean" or "Byte" or "SByte" or "Int16" or "UInt16" or
            "Int32" or "UInt32" or "Int64" or "UInt64" or
            "Single" or "Double" or "Decimal" or "String" or "Char" or
            "DateTime" or "DateTimeOffset" or "TimeSpan" or "DateOnly" or "TimeOnly" or
            "Guid" or "Object" => true,
            _ => false
        };
    }

    /// <summary>
    /// Sanitizes a tuple type name by stripping element names and keeping only types.
    /// For example, "(string Status, int)" becomes "(string, int)".
    /// This prevents element names from being emitted as types in interceptor signatures
    /// when the semantic model produces unresolved element types.
    /// </summary>
    private static string SanitizeTupleResultType(string tupleType)
    {
        // Delegate to the full implementation
        return SanitizeTupleResultTypeCore(tupleType);
    }

    /// <summary>
    /// Resolves the best available result type for execution interceptors.
    /// Prefers the semantic model's type (most qualified) but falls back to
    /// enriched ProjectionInfo type for tuples, single-column, and empty results.
    /// </summary>
    private static string? ResolveExecutionResultType(
        string? siteResultType,
        string? chainResultType,
        ProjectionInfo? projectionInfo)
    {
        // For non-tuple, non-empty results, prefer the semantic model's type (most qualified)
        if (!string.IsNullOrEmpty(siteResultType)
            && !siteResultType!.Contains("(") && !siteResultType.Contains("ValueTuple"))
            return siteResultType;
        if (!string.IsNullOrEmpty(chainResultType)
            && !chainResultType!.Contains("(") && !chainResultType.Contains("ValueTuple"))
            return chainResultType;

        // For tuples, empty results, or broken types: use enriched projection type
        var projResult = projectionInfo?.ResultTypeName;
        if (!string.IsNullOrEmpty(projResult))
            return projResult;

        // Last resort: return whatever we have (may be empty/broken)
        return string.IsNullOrEmpty(siteResultType) ? chainResultType : siteResultType;
    }

    /// <summary>
    /// Strips tuple element names from a C# tuple type, keeping only the type parts.
    /// For example, "(string Status, int)" becomes "(string, int)".
    /// </summary>
    private static string SanitizeTupleResultTypeCore(string tupleType)
    {
        if (!tupleType.StartsWith("(") || !tupleType.EndsWith(")"))
            return tupleType;

        var inner = tupleType.Substring(1, tupleType.Length - 2);
        var parts = inner.Split(',');
        var sanitized = new string[parts.Length];

        for (int i = 0; i < parts.Length; i++)
        {
            var trimmed = parts[i].Trim();
            var spaceIdx = trimmed.IndexOf(' ');
            if (spaceIdx > 0)
            {
                var typePart = trimmed.Substring(0, spaceIdx);
                var namePart = trimmed.Substring(spaceIdx + 1);

                // If the "type" part is empty/whitespace or looks like a name without a type,
                // then the whole token is just a name — use "object" as fallback
                if (string.IsNullOrWhiteSpace(typePart))
                {
                    sanitized[i] = "object";
                }
                else
                {
                    // Strip the element name, keep only the type
                    sanitized[i] = typePart;
                }
            }
            else
            {
                // Single word — could be a type ("int") or a misplaced name ("Status")
                if (trimmed.Length > 0 && char.IsUpper(trimmed[0]) &&
                    !trimmed.Contains('.') && !IsKnownUppercaseTypeName(trimmed))
                {
                    // Likely a name, not a type — use "object"
                    sanitized[i] = "object";
                }
                else
                {
                    sanitized[i] = trimmed;
                }
            }
        }

        return $"({string.Join(", ", sanitized)})";
    }

    /// <summary>
    /// Generates an interceptor method for a usage site.
    /// </summary>
    private static void GenerateInterceptorMethod(
        StringBuilder sb,
        UsageSiteInfo site,
        List<CachedExtractorField> staticFields,
        Dictionary<string, PrebuiltChainInfo> chainLookup,
        Dictionary<string, int> clauseBitMap,
        Dictionary<string, PrebuiltChainInfo> chainClauseLookup,
        HashSet<string> firstClauseIds)
    {
        // Check for skippable Select sites BEFORE emitting the attribute
        if (site.Kind == InterceptorKind.Select && ShouldSkipSelectInterceptor(site))
        {
            return; // Don't emit anything for this site
        }

        // Limit/Offset/Distinct are tracked for chain analysis only — they don't need
        // their own interceptor methods. The builder methods are simple property setters.
        if (site.Kind is InterceptorKind.Limit or InterceptorKind.Offset or InterceptorKind.Distinct)
        {
            return;
        }

        // For execution interceptors: only emit if we have a pre-built chain for this site
        // AND we can resolve the result type. Otherwise skip — the built-in execution methods
        // work correctly via the Select interceptor.
        if (site.Kind is InterceptorKind.ExecuteFetchAll or InterceptorKind.ExecuteFetchFirst
            or InterceptorKind.ExecuteFetchFirstOrDefault or InterceptorKind.ExecuteFetchSingle
            or InterceptorKind.ToAsyncEnumerable)
        {
            if (!chainLookup.TryGetValue(site.UniqueId, out var chain))
                return;
            // Skip chains with unmatched fluent methods (Limit, Offset, AddWhereClause, etc.)
            // whose effects are invisible to the pre-built SQL.
            if (chain.Analysis.UnmatchedMethodNames != null)
                return;
            // Resolve the best available result type. The site/chain result types come from the
            // semantic model and may be empty or broken for source-generated entity types.
            // The enriched ProjectionInfo.ResultTypeName is rebuilt from entity metadata and is
            // authoritative for tuples and single-column projections.
            var rawResult = ResolveExecutionResultType(site.ResultTypeName, chain.ResultTypeName, chain.ProjectionInfo);
            if (string.IsNullOrEmpty(rawResult))
                return;
            // Must have a reader delegate
            if (chain.ReaderDelegateCode == null)
                return;
            // Skip chains with projections that have SqlExpression set alongside ColumnName.
            // CompileTimeSqlBuilder uses SqlExpression (e.g., "LOWER(col)") but the runtime path
            // uses ColumnName and quotes it. These produce different SQL.
            // Only allow execution interceptors when the SQL will be byte-identical.
            if (chain.ProjectionInfo != null)
            {
                var hasAmbiguousColumns = chain.ProjectionInfo.Columns.Any(c =>
                    c.SqlExpression != null && !string.IsNullOrEmpty(c.ColumnName));
                if (hasAmbiguousColumns)
                    return;
            }
        }
        else if (site.Kind is InterceptorKind.ExecuteScalar)
        {
            if (!chainLookup.TryGetValue(site.UniqueId, out var scalarChain))
                return;
            if (scalarChain.Analysis.UnmatchedMethodNames != null)
                return;
            // ExecuteScalar doesn't need a reader delegate — just pre-built SQL
            var rawScalarResult = ResolveExecutionResultType(site.ResultTypeName, scalarChain.ResultTypeName, scalarChain.ProjectionInfo);
            if (string.IsNullOrEmpty(rawScalarResult))
                return;
        }
        else if (site.Kind is InterceptorKind.ExecuteNonQuery)
        {
            if (!chainLookup.TryGetValue(site.UniqueId, out var nqChain))
                return;
            // Validate that all SQL variants are non-empty and well-formed
            if (nqChain.SqlMap.Values.Any(v => string.IsNullOrWhiteSpace(v.Sql)
                || (nqChain.QueryKind == QueryKind.Update && v.Sql.Contains("SET  "))))
                return;
        }
        else if (site.Kind is InterceptorKind.ToSql)
        {
            if (!chainLookup.TryGetValue(site.UniqueId, out var toSqlChain))
                return;
            if (toSqlChain.Analysis.UnmatchedMethodNames != null)
                return;
        }

        // Skip clause interceptors where the clause could not be translated to SQL.
        // The original runtime method will run instead. A QRY019 diagnostic is reported separately.
        if (ShouldSkipNonTranslatableClause(site))
        {
            return;
        }

        var methodName = $"{site.MethodName}_{site.UniqueId}";

        // Determine chain analysis status for remarks
        PrebuiltChainInfo? chainForRemarks = null;
        chainLookup.TryGetValue(site.UniqueId, out chainForRemarks);
        if (chainForRemarks == null)
            chainClauseLookup.TryGetValue(site.UniqueId, out chainForRemarks);

        // XML documentation
        sb.AppendLine($"    /// <summary>");
        sb.AppendLine($"    /// Intercepts {site.MethodName}() call at {GetRelativePath(site.FilePath)}:{site.Line}:{site.Column}");
        sb.AppendLine($"    /// </summary>");
        if (chainForRemarks != null)
            sb.AppendLine($"    /// <remarks>Chain: Fully Analyzed ({chainForRemarks.Analysis.Tier})</remarks>");
        else
            sb.AppendLine($"    /// <remarks>Chain: Not Analyzed (standalone interceptor)</remarks>");

        // InterceptsLocation attribute - use new format with version and data if available
        if (!string.IsNullOrEmpty(site.InterceptableLocationData))
        {
            sb.AppendLine($"    [InterceptsLocation({site.InterceptableLocationVersion}, \"{site.InterceptableLocationData}\")]");
        }
        else
        {
            // Fallback: skip this site if we don't have valid location data
            sb.AppendLine($"    // WARNING: Could not generate InterceptsLocation for this call site");
            return;
        }

        // Check if this is on a joined builder type
        var isJoinedBuilder = site.JoinedEntityTypeNames != null && site.JoinedEntityTypeNames.Count >= 2;

        // Look up clause bitmask bit index for conditional clause interceptors
        clauseBitMap.TryGetValue(site.UniqueId, out var rawBit);
        int? clauseBit = clauseBitMap.ContainsKey(site.UniqueId) ? rawBit : null;

        // Resolve prebuilt chain membership for clause sites
        chainClauseLookup.TryGetValue(site.UniqueId, out var prebuiltClauseChain);
        var isFirstClauseInChain = prebuiltClauseChain != null && firstClauseIds.Contains(site.UniqueId);

        // Generate method based on kind
        switch (site.Kind)
        {
            case InterceptorKind.Select:
                if (isJoinedBuilder)
                    GenerateJoinedSelectInterceptor(sb, site, methodName, prebuiltClauseChain, isFirstClauseInChain);
                else
                    GenerateSelectInterceptor(sb, site, methodName, prebuiltClauseChain, isFirstClauseInChain);
                break;

            case InterceptorKind.Where:
                if (isJoinedBuilder)
                    GenerateJoinedWhereInterceptor(sb, site, methodName, staticFields, clauseBit, prebuiltClauseChain, isFirstClauseInChain);
                else
                    GenerateWhereInterceptor(sb, site, methodName, staticFields, clauseBit, prebuiltClauseChain, isFirstClauseInChain);
                break;

            case InterceptorKind.OrderBy:
            case InterceptorKind.ThenBy:
                if (isJoinedBuilder)
                    GenerateJoinedOrderByInterceptor(sb, site, methodName, clauseBit, prebuiltClauseChain, isFirstClauseInChain);
                else
                    GenerateOrderByInterceptor(sb, site, methodName, clauseBit, prebuiltClauseChain, isFirstClauseInChain);
                break;

            case InterceptorKind.GroupBy:
                GenerateGroupByInterceptor(sb, site, methodName, clauseBit, prebuiltClauseChain, isFirstClauseInChain);
                break;

            case InterceptorKind.Having:
                GenerateHavingInterceptor(sb, site, methodName, clauseBit, prebuiltClauseChain, isFirstClauseInChain);
                break;

            case InterceptorKind.Set:
                GenerateSetInterceptor(sb, site, methodName, clauseBit, prebuiltClauseChain, isFirstClauseInChain);
                break;

            case InterceptorKind.Join:
            case InterceptorKind.LeftJoin:
            case InterceptorKind.RightJoin:
                GenerateJoinInterceptor(sb, site, methodName);
                break;

            case InterceptorKind.ExecuteFetchAll:
            case InterceptorKind.ExecuteFetchFirst:
            case InterceptorKind.ExecuteFetchFirstOrDefault:
            case InterceptorKind.ExecuteFetchSingle:
            case InterceptorKind.ExecuteScalar:
            case InterceptorKind.ToAsyncEnumerable:
                if (chainLookup.TryGetValue(site.UniqueId, out var selectChain))
                {
                    if (selectChain.IsJoinChain)
                        GeneratePrebuiltJoinExecutionInterceptor(sb, site, methodName, selectChain);
                    else
                        GeneratePrebuiltSelectExecutionInterceptor(sb, site, methodName, selectChain);
                }
                break;

            case InterceptorKind.ExecuteNonQuery:
                if (chainLookup.TryGetValue(site.UniqueId, out var nonQueryChain))
                    GeneratePrebuiltNonQueryExecutionInterceptor(sb, site, methodName, nonQueryChain);
                break;

            case InterceptorKind.ToSql:
                if (chainLookup.TryGetValue(site.UniqueId, out var toSqlChain))
                    GeneratePrebuiltToSqlInterceptor(sb, site, methodName, toSqlChain);
                break;

            case InterceptorKind.DeleteWhere:
                GenerateDeleteWhereInterceptor(sb, site, methodName, staticFields, clauseBit, prebuiltClauseChain, isFirstClauseInChain);
                break;

            case InterceptorKind.UpdateSet:
                GenerateUpdateSetInterceptor(sb, site, methodName, clauseBit, prebuiltClauseChain, isFirstClauseInChain);
                break;

            case InterceptorKind.UpdateSetPoco:
                GenerateUpdateSetPocoInterceptor(sb, site, methodName);
                break;

            case InterceptorKind.UpdateWhere:
                GenerateUpdateWhereInterceptor(sb, site, methodName, staticFields, clauseBit, prebuiltClauseChain, isFirstClauseInChain);
                break;

            case InterceptorKind.InsertExecuteNonQuery:
                GenerateInsertExecuteNonQueryInterceptor(sb, site, methodName);
                break;

            case InterceptorKind.InsertExecuteScalar:
                GenerateInsertExecuteScalarInterceptor(sb, site, methodName);
                break;

            case InterceptorKind.InsertToSql:
                GenerateInsertToSqlInterceptor(sb, site, methodName);
                break;

            case InterceptorKind.RawSqlAsync:
                GenerateRawSqlAsyncInterceptor(sb, site, methodName);
                break;

            case InterceptorKind.RawSqlScalarAsync:
                GenerateRawSqlScalarAsyncInterceptor(sb, site, methodName);
                break;

            default:
                GeneratePlaceholderInterceptor(sb, site, methodName);
                break;
        }

        sb.AppendLine();
    }

    /// <summary>
    /// Generates a Select() interceptor with column list and typed reader delegate.
    /// </summary>
    private static void GenerateSelectInterceptor(StringBuilder sb, UsageSiteInfo site, string methodName,
        PrebuiltChainInfo? prebuiltChain = null, bool isFirstInChain = false)
    {
        var entityType = GetShortTypeName(site.EntityTypeName);
        var projection = site.ProjectionInfo;

        // Simplified prebuilt chain path: AsProjected instead of SelectWithReader
        if (prebuiltChain != null && projection != null && projection.IsOptimalPath && projection.Columns.Count > 0)
        {
            var resultType = GetShortTypeName(projection.ResultTypeName);
            var thisType = site.BuilderTypeName;
            var concreteType = ToConcreteTypeName(thisType);
            sb.AppendLine($"    public static {thisType}<{entityType}, {resultType}> {methodName}(");
            sb.AppendLine($"        this {thisType}<{entityType}> builder,");
            sb.AppendLine($"        Func<{entityType}, {resultType}> _)");
            sb.AppendLine($"    {{");
            sb.AppendLine($"        var __b = Unsafe.As<{concreteType}<{entityType}>>(builder);");
            if (isFirstInChain && prebuiltChain.MaxParameterCount > 0)
                sb.AppendLine($"        __b.AllocatePrebuiltParams({prebuiltChain.MaxParameterCount});");
            sb.AppendLine($"        return __b.AsProjected<{resultType}>();");
            sb.AppendLine($"    }}");
            return;
        }

        // If we have projection info and it's optimal, generate the full interceptor
        if (projection != null &&
            projection.IsOptimalPath &&
            projection.Columns.Count > 0)
        {
            GenerateOptimalSelectInterceptor(sb, site, methodName, entityType, projection);
        }
        else
        {
            // Fallback - delegate to the original method
            GenerateFallbackSelectInterceptor(sb, site, methodName, entityType);
        }
    }

    /// <summary>
    /// Generates an optimal Select() interceptor with compile-time column binding.
    /// </summary>
    private static void GenerateOptimalSelectInterceptor(
        StringBuilder sb,
        UsageSiteInfo site,
        string methodName,
        string entityType,
        ProjectionInfo projection)
    {
        // Generate column list SQL using the dialect resolved from the owning context
        var columnList = ReaderCodeGenerator.GenerateColumnList(projection, site.Dialect ?? SqlDialect.PostgreSQL);

        // Generate column names array
        var columnNames = ReaderCodeGenerator.GenerateColumnNamesArray(projection);

        // Generate reader delegate
        var readerDelegate = ReaderCodeGenerator.GenerateReaderDelegate(projection, entityType);

        // At this point, skippable cases (anonymous, tuple, unknown) have already been
        // filtered out by ShouldSkipSelectInterceptor, so we can generate the interceptor
        {
            var resultType = GetShortTypeName(projection.ResultTypeName);
            var thisType = site.BuilderTypeName;
            var concreteType = ToConcreteTypeName(thisType);
            sb.AppendLine($"    public static {thisType}<{entityType}, {resultType}> {methodName}(");
            sb.AppendLine($"        this {thisType}<{entityType}> builder,");
            sb.AppendLine($"        Func<{entityType}, {resultType}> _)");
            sb.AppendLine($"    {{");
            sb.AppendLine($"        // Generated column list: {columnList}");
            sb.AppendLine($"        var __b = Unsafe.As<{concreteType}<{entityType}>>(builder);");
            sb.AppendLine($"        return __b.SelectWithReader(");
            sb.AppendLine($"            {columnNames},");
            sb.AppendLine($"            {readerDelegate});");
            sb.AppendLine($"    }}");
        }
    }

    /// <summary>
    /// Generates a fallback Select() interceptor that delegates to the original method.
    /// </summary>
    private static void GenerateFallbackSelectInterceptor(
        StringBuilder sb,
        UsageSiteInfo site,
        string methodName,
        string entityType)
    {
        // Interceptor arity must match the combined class + method type parameters.
        // QueryBuilder<T>.Select<TResult> has total arity 2 (T + TResult).
        // Use arity 2 so the interceptor can match any Select<TResult> call.
        var thisType = site.BuilderTypeName;
        var concreteType = ToConcreteTypeName(thisType);
        sb.AppendLine($"    public static {thisType}<T, TResult> {methodName}<T, TResult>(");
        sb.AppendLine($"        this {thisType}<T> builder,");
        sb.AppendLine($"        Func<T, TResult> selector) where T : class");
        sb.AppendLine($"    {{");
        sb.AppendLine($"        // Fallback path - projection not fully analyzed at compile time");
        sb.AppendLine($"        var __b = Unsafe.As<{concreteType}<T>>(builder);");
        sb.AppendLine($"        return __b.Select(selector);");
        sb.AppendLine($"    }}");
    }

    /// <summary>
    /// Generates a Where() interceptor with SQL fragment and parameter binder.
    /// </summary>
    private static void GenerateWhereInterceptor(StringBuilder sb, UsageSiteInfo site, string methodName, List<CachedExtractorField> staticFields, int? clauseBit = null,
        PrebuiltChainInfo? prebuiltChain = null, bool isFirstInChain = false)
    {
        var entityType = GetShortTypeName(site.EntityTypeName);
        var clauseInfo = site.ClauseInfo;

        // Check if there are captured parameters that need runtime extraction
        var hasAnyParams = clauseInfo?.Parameters.Count > 0;
        var hasResolvableCapturedParams = clauseInfo?.Parameters.Any(p => p.IsCaptured && p.CanGenerateDirectPath) == true;
        var exprParamName = hasResolvableCapturedParams ? "expr" : "_";

        // Emit trim suppression if we'll use FieldInfo.GetValue inline
        var methodFields = staticFields.Where(f => f.MethodName == methodName).ToList();
        if (methodFields.Count > 0)
        {
            sb.AppendLine($"    [UnconditionalSuppressMessage(\"Trimming\", \"IL2075\",");
            sb.AppendLine($"        Justification = \"Closure fields are preserved by the expression tree that references them.\")]");
        }

        var thisType = site.BuilderTypeName;
        var concreteType = ToConcreteTypeName(thisType);

        // Check if this is on QueryBuilder<T> or QueryBuilder<T, TResult>
        if (site.ResultTypeName != null)
        {
            var resultType = SanitizeTupleResultType(GetShortTypeName(site.ResultTypeName));
            sb.AppendLine($"    public static {thisType}<{entityType}, {resultType}> {methodName}(");
            sb.AppendLine($"        this {thisType}<{entityType}, {resultType}> builder,");
            sb.AppendLine($"        Expression<Func<{entityType}, bool>> {exprParamName})");
        }
        else
        {
            sb.AppendLine($"    public static {thisType}<{entityType}> {methodName}(");
            sb.AppendLine($"        this {thisType}<{entityType}> builder,");
            sb.AppendLine($"        Expression<Func<{entityType}, bool>> {exprParamName})");
        }

        sb.AppendLine($"    {{");

        if (clauseInfo == null || !clauseInfo.IsSuccess)
        {
            // Non-translatable clause — skip interceptor entirely so the original method runs
            return;
        }

        // Cast to concrete type (receiver is always an interface)
        if (site.ResultTypeName != null)
        {
            var resultType = SanitizeTupleResultType(GetShortTypeName(site.ResultTypeName));
            sb.AppendLine($"        var __b = Unsafe.As<{concreteType}<{entityType}, {resultType}>>(builder);");
        }
        else
        {
            sb.AppendLine($"        var __b = Unsafe.As<{concreteType}<{entityType}>>(builder);");
        }
        var builderVar = "__b";

        // Simplified prebuilt chain path: BindParam instead of AddWhereClause
        if (prebuiltChain != null)
        {
            if (isFirstInChain && prebuiltChain.MaxParameterCount > 0)
                sb.AppendLine($"        {builderVar}.AllocatePrebuiltParams({prebuiltChain.MaxParameterCount});");

            if (hasAnyParams == true)
            {
                if (hasResolvableCapturedParams)
                    GenerateCachedExtraction(sb, methodFields);

                var allParams = clauseInfo!.Parameters.OrderBy(p => p.Index).ToList();
                foreach (var p in allParams)
                {
                    var expr = p.IsCaptured ? $"p{p.Index}" : p.ValueExpression;
                    sb.AppendLine($"        {builderVar}.BindParam({WrapWithToDb(expr, p)});");
                }
            }

            if (clauseBit.HasValue)
                sb.AppendLine($"        return {builderVar}.SetClauseBit({clauseBit.Value});");
            else
                sb.AppendLine($"        return {builderVar};");
            sb.AppendLine($"    }}");
            return;
        }

        {
            // Generate optimized interceptor with pre-computed SQL
            var escapedSql = EscapeStringLiteral(clauseInfo.SqlFragment);

            // Check if any captured parameters lack extraction paths (e.g., captured variables in subqueries)
            var hasUnresolvableCaptured = clauseInfo.Parameters.Any(p => p.IsCaptured && !p.CanGenerateDirectPath);

            var bitSuffix = ClauseBitSuffix(clauseBit);
            if (hasUnresolvableCaptured)
            {
                // Emit SQL-only clause (parameters cannot be extracted at compile time)
                var resolvableParams = clauseInfo.Parameters
                    .Where(p => !p.IsCaptured)
                    .OrderBy(p => p.Index)
                    .ToList();
                if (resolvableParams.Count > 0)
                {
                    var paramArgs = string.Join(", ", resolvableParams.Select(p => WrapWithToDb(p.ValueExpression, p)));
                    sb.AppendLine($"        return {builderVar}.AddWhereClause(@\"{escapedSql}\", {paramArgs}){bitSuffix};");
                }
                else
                {
                    sb.AppendLine($"        return {builderVar}.AddWhereClause(@\"{escapedSql}\"){bitSuffix};");
                }
            }
            else if (hasAnyParams)
            {
                if (hasResolvableCapturedParams)
                    GenerateCachedExtraction(sb, methodFields);

                // Build args in index order, mixing captured extractions + literal values
                var allParams = clauseInfo.Parameters.OrderBy(p => p.Index).ToList();
                var paramArgs = string.Join(", ", allParams.Select(p =>
                {
                    var expr = p.IsCaptured ? $"p{p.Index}" : p.ValueExpression;
                    return WrapWithToDb(expr, p);
                }));
                sb.AppendLine($"        return {builderVar}.AddWhereClause(@\"{escapedSql}\", {paramArgs}){bitSuffix};");
            }
            else
            {
                sb.AppendLine($"        return {builderVar}.AddWhereClause(@\"{escapedSql}\"){bitSuffix};");
            }
        }

        sb.AppendLine($"    }}");
    }

    /// <summary>
    /// Generates a Where() interceptor for DeleteBuilder or ExecutableDeleteBuilder.
    /// The return type is always IExecutableDeleteBuilder&lt;T&gt; since Where() on
    /// DeleteBuilder returns IExecutableDeleteBuilder, and on ExecutableDeleteBuilder returns itself.
    /// </summary>
    private static void GenerateDeleteWhereInterceptor(StringBuilder sb, UsageSiteInfo site, string methodName, List<CachedExtractorField> staticFields, int? clauseBit = null,
        PrebuiltChainInfo? prebuiltChain = null, bool isFirstInChain = false)
    {
        var entityType = GetShortTypeName(site.EntityTypeName);
        var clauseInfo = site.ClauseInfo;

        // Check if there are captured parameters that need runtime extraction
        var hasAnyParams = clauseInfo?.Parameters.Count > 0;
        var hasResolvableCapturedParams = clauseInfo?.Parameters.Any(p => p.IsCaptured && p.CanGenerateDirectPath) == true;
        var exprParamName = hasResolvableCapturedParams ? "expr" : "_";

        // Emit trim suppression if we'll use FieldInfo.GetValue inline
        var methodFields = staticFields.Where(f => f.MethodName == methodName).ToList();
        if (methodFields.Count > 0)
        {
            sb.AppendLine($"    [UnconditionalSuppressMessage(\"Trimming\", \"IL2075\",");
            sb.AppendLine($"        Justification = \"Closure fields are preserved by the expression tree that references them.\")]");
        }

        // Determine the receiver type: DeleteBuilder<T> or ExecutableDeleteBuilder<T> (or interface variants)
        var thisType = site.BuilderTypeName;
        var isExecutable = thisType.Contains("ExecutableDeleteBuilder");
        var concreteType = ToConcreteTypeName(thisType);
        var receiverType = $"{thisType}<{entityType}>";

        sb.AppendLine($"    public static IExecutableDeleteBuilder<{entityType}> {methodName}(");
        sb.AppendLine($"        this {receiverType} builder,");
        sb.AppendLine($"        Expression<Func<{entityType}, bool>> {exprParamName})");
        sb.AppendLine($"    {{");

        if (clauseInfo == null || !clauseInfo.IsSuccess)
        {
            // Non-translatable clause — skip interceptor entirely so the original method runs
            return;
        }

        // Cast to concrete type (receiver is always an interface)
        sb.AppendLine($"        var __b = Unsafe.As<{concreteType}<{entityType}>>(builder);");
        var builderVar = "__b";

        // Simplified prebuilt chain path: BindParam instead of AddWhereClause
        if (prebuiltChain != null)
        {
            if (isFirstInChain && prebuiltChain.MaxParameterCount > 0)
                sb.AppendLine($"        {builderVar}.AllocatePrebuiltParams({prebuiltChain.MaxParameterCount});");

            if (hasAnyParams == true)
            {
                if (hasResolvableCapturedParams)
                    GenerateCachedExtraction(sb, methodFields);

                var allParams = clauseInfo!.Parameters.OrderBy(p => p.Index).ToList();
                foreach (var p in allParams)
                {
                    var expr = p.IsCaptured ? $"p{p.Index}" : p.ValueExpression;
                    sb.AppendLine($"        {builderVar}.BindParam({WrapWithToDb(expr, p)});");
                }
            }

            // Non-executable receiver (IDeleteBuilder) must transition to executable via AsExecutable()
            var returnExpr = isExecutable ? builderVar : $"{builderVar}.AsExecutable()";
            if (clauseBit.HasValue)
                sb.AppendLine($"        return {returnExpr}.SetClauseBit({clauseBit.Value});");
            else
                sb.AppendLine($"        return {returnExpr};");
            sb.AppendLine($"    }}");
            return;
        }

        {
            // Generate optimized interceptor with pre-computed SQL
            var escapedSql = EscapeStringLiteral(clauseInfo.SqlFragment);

            // Check if any captured parameters lack extraction paths
            var hasUnresolvableCaptured = clauseInfo.Parameters.Any(p => p.IsCaptured && !p.CanGenerateDirectPath);
            var bitSuffix = ClauseBitSuffix(clauseBit);

            if (hasUnresolvableCaptured)
            {
                // Emit SQL-only clause (parameters cannot be extracted at compile time)
                var resolvableParams = clauseInfo.Parameters
                    .Where(p => !p.IsCaptured)
                    .OrderBy(p => p.Index)
                    .ToList();
                if (resolvableParams.Count > 0)
                {
                    // Use AddParameter with index capture so @pN placeholders account for
                    // any prior parameters
                    var transformedSql = escapedSql;
                    for (int i = resolvableParams.Count - 1; i >= 0; i--)
                    {
                        var p = resolvableParams[i];
                        sb.AppendLine($"        var _pi{p.Index} = {builderVar}.AddParameter({WrapWithToDb(p.ValueExpression, p)});");
                        transformedSql = transformedSql.Replace($"@p{p.Index}", $"@p{{_pi{p.Index}}}");
                    }
                    sb.AppendLine($"        return {builderVar}.AddWhereClause($@\"{transformedSql}\"){bitSuffix};");
                }
                else
                {
                    sb.AppendLine($"        return {builderVar}.AddWhereClause(@\"{escapedSql}\"){bitSuffix};");
                }
            }
            else if (hasAnyParams)
            {
                if (hasResolvableCapturedParams)
                    GenerateCachedExtraction(sb, methodFields);

                // Add parameters via AddParameter, capturing the runtime index so @pN
                // placeholders account for any prior parameters
                var allParams = clauseInfo.Parameters.OrderBy(p => p.Index).ToList();
                var transformedSql = escapedSql;
                for (int i = allParams.Count - 1; i >= 0; i--)
                {
                    var p = allParams[i];
                    var valueExpr = p.IsCaptured ? $"p{p.Index}" : p.ValueExpression;
                    sb.AppendLine($"        var _pi{p.Index} = {builderVar}.AddParameter({WrapWithToDb(valueExpr, p)});");
                    transformedSql = transformedSql.Replace($"@p{p.Index}", $"@p{{_pi{p.Index}}}");
                }
                sb.AppendLine($"        return {builderVar}.AddWhereClause($@\"{transformedSql}\"){bitSuffix};");
            }
            else
            {
                sb.AppendLine($"        return {builderVar}.AddWhereClause(@\"{escapedSql}\"){bitSuffix};");
            }
        }

        sb.AppendLine($"    }}");
    }

    /// <summary>
    /// Generates inline expression tree navigation code for a path segment.
    /// Each segment maps to a specific Unsafe.As cast and property access.
    /// </summary>
    private static string GenerateSegmentNavigation(string segment, string prevExpr)
    {
        // Handle Arguments[N] format
        if (segment.StartsWith("Arguments["))
        {
            return $"Unsafe.As<MethodCallExpression>({prevExpr}).Arguments[{segment.Substring("Arguments[".Length, 1)}]";
        }

        return segment switch
        {
            "Body" => $"{prevExpr}.Body",
            "Left" => $"Unsafe.As<BinaryExpression>({prevExpr}).Left",
            "Right" => $"Unsafe.As<BinaryExpression>({prevExpr}).Right",
            "Operand" => $"Unsafe.As<UnaryExpression>({prevExpr}).Operand",
            "Object" => $"Unsafe.As<MethodCallExpression>({prevExpr}).Object!",
            "Expression" => $"Unsafe.As<MemberExpression>({prevExpr}).Expression!",
            "Test" => $"Unsafe.As<ConditionalExpression>({prevExpr}).Test",
            "IfTrue" => $"Unsafe.As<ConditionalExpression>({prevExpr}).IfTrue",
            "IfFalse" => $"Unsafe.As<ConditionalExpression>({prevExpr}).IfFalse",
            _ => throw new InvalidOperationException($"Unknown path segment: {segment}")
        };
    }

    /// <summary>
    /// Generates inline navigation code that arrives at the MemberExpression for a given path.
    /// Returns the variable name holding the final MemberExpression.
    /// </summary>
    /// <remarks>
    /// For enum captured variables, the C# compiler inserts a UnaryExpression(Convert) node
    /// wrapping the MemberExpression. The generated code uses a pattern match to unwrap it.
    /// </remarks>
    private static string GenerateInlineNavigation(StringBuilder sb, string path, int paramIndex)
    {
        var segments = path.Split('.');
        // "Body" is the first segment — navigate from expr
        // The remaining segments (except the last, which is the MemberExpression itself)
        // are intermediate casts. We build a single chained expression.

        // Build the chain from the inside out
        var currentExpr = "expr";
        foreach (var segment in segments)
        {
            currentExpr = GenerateSegmentNavigation(segment, currentExpr);
        }

        // The final node is a MemberExpression, but for enum captured variables
        // the compiler inserts a UnaryExpression(Convert) wrapper that must be stripped.
        var nodeVar = $"_n{paramIndex}";
        var memberVar = $"_m{paramIndex}";
        sb.AppendLine($"        var {nodeVar} = {currentExpr};");
        sb.AppendLine($"        var {memberVar} = {nodeVar} is UnaryExpression _u{paramIndex} ? Unsafe.As<MemberExpression>(_u{paramIndex}.Operand) : Unsafe.As<MemberExpression>({nodeVar});");
        return memberVar;
    }

    /// <summary>
    /// Generates cached extractor code using static fields for all captured parameters.
    /// Emits inline Unsafe.As navigation with cached FieldInfo.
    /// </summary>
    private static void GenerateCachedExtraction(StringBuilder sb, List<CachedExtractorField> fields)
    {
        foreach (var field in fields)
        {
            sb.AppendLine($"        // Inline extraction: {field.ExpressionPath}");
            var memberVar = GenerateInlineNavigation(sb, field.ExpressionPath, field.ParameterIndex);
            sb.AppendLine($"        {field.FieldName} ??= Unsafe.As<FieldInfo>({memberVar}.Member);");
            sb.AppendLine($"        var p{field.ParameterIndex} = {field.FieldName}.GetValue(Unsafe.As<ConstantExpression>({memberVar}.Expression!).Value);");
        }
    }

    /// <summary>
    /// Generates an OrderBy/ThenBy interceptor with SQL fragment.
    /// </summary>
    private static void GenerateOrderByInterceptor(StringBuilder sb, UsageSiteInfo site, string methodName, int? clauseBit = null,
        PrebuiltChainInfo? prebuiltChain = null, bool isFirstInChain = false)
    {
        var entityType = GetShortTypeName(site.EntityTypeName);
        var clauseInfo = site.ClauseInfo;
        var isOrderBy = site.Kind == InterceptorKind.OrderBy;

        // Use concrete key type (arity 0) when available; otherwise fall back to arity-matching
        var keyType = site.KeyTypeName != null ? GetShortTypeName(site.KeyTypeName) : null;

        var thisType = site.BuilderTypeName;
        var concreteType = ToConcreteTypeName(thisType);

        // Check if this is on QueryBuilder<T> or QueryBuilder<T, TResult>
        if (site.ResultTypeName != null)
        {
            var resultType = SanitizeTupleResultType(GetShortTypeName(site.ResultTypeName));
            if (keyType != null)
            {
                // Non-generic interceptor (arity 0) — concrete key type
                sb.AppendLine($"    public static {thisType}<{entityType}, {resultType}> {methodName}(");
                sb.AppendLine($"        this {thisType}<{entityType}, {resultType}> builder,");
                sb.AppendLine($"        Expression<Func<{entityType}, {keyType}>> _,");
                sb.AppendLine($"        Direction direction = Direction.Ascending)");
            }
            else
            {
                // Arity-matching interceptor — include all type params from class + method
                sb.AppendLine($"    public static {thisType}<T, TResult> {methodName}<T, TResult, TKey>(");
                sb.AppendLine($"        this {thisType}<T, TResult> builder,");
                sb.AppendLine($"        Expression<Func<T, TKey>> _,");
                sb.AppendLine($"        Direction direction = Direction.Ascending) where T : class");
            }
        }
        else
        {
            if (keyType != null)
            {
                // Non-generic interceptor (arity 0) — concrete key type
                sb.AppendLine($"    public static {thisType}<{entityType}> {methodName}(");
                sb.AppendLine($"        this {thisType}<{entityType}> builder,");
                sb.AppendLine($"        Expression<Func<{entityType}, {keyType}>> _,");
                sb.AppendLine($"        Direction direction = Direction.Ascending)");
            }
            else
            {
                // Arity-matching interceptor — include all type params from class + method
                sb.AppendLine($"    public static {thisType}<T> {methodName}<T, TKey>(");
                sb.AppendLine($"        this {thisType}<T> builder,");
                sb.AppendLine($"        Expression<Func<T, TKey>> _,");
                sb.AppendLine($"        Direction direction = Direction.Ascending) where T : class");
            }
        }

        sb.AppendLine($"    {{");

        // Cast to concrete type (receiver is always an interface)
        if (site.ResultTypeName != null)
        {
            var resultType = SanitizeTupleResultType(GetShortTypeName(site.ResultTypeName));
            sb.AppendLine($"        var __b = Unsafe.As<{concreteType}<{entityType}, {resultType}>>(builder);");
        }
        else if (keyType != null)
        {
            sb.AppendLine($"        var __b = Unsafe.As<{concreteType}<{entityType}>>(builder);");
        }
        else
        {
            sb.AppendLine($"        var __b = Unsafe.As<{concreteType}<T>>(builder);");
        }
        var builderVar = "__b";

        // Simplified prebuilt chain path: passthrough (no SQL generation)
        if (prebuiltChain != null)
        {
            if (isFirstInChain && prebuiltChain.MaxParameterCount > 0)
                sb.AppendLine($"        {builderVar}.AllocatePrebuiltParams({prebuiltChain.MaxParameterCount});");
            if (clauseBit.HasValue)
            {
                sb.AppendLine($"        {builderVar}.SetClauseBit({clauseBit.Value});");
                sb.AppendLine($"        return builder;");
            }
            else
            {
                sb.AppendLine($"        return builder;");
            }
            sb.AppendLine($"    }}");
            return;
        }

        // When using open generic type params (arity-matching), the concrete return type
        // can't be implicitly converted to the open generic interface return type.
        // Cast through Unsafe.As to bridge the gap.
        var needsReturnCast = keyType == null;
        string returnCastOpen;
        string returnCastClose;
        if (needsReturnCast)
        {
            var returnType = site.ResultTypeName != null ? $"{thisType}<T, TResult>" : $"{thisType}<T>";
            returnCastOpen = $"Unsafe.As<{returnType}>(";
            returnCastClose = ")";
        }
        else
        {
            returnCastOpen = "";
            returnCastClose = "";
        }

        var bitSuffix = ClauseBitSuffix(clauseBit);
        if (clauseInfo is OrderByClauseInfo orderByInfo && orderByInfo.IsSuccess)
        {
            // Generate optimized interceptor with pre-computed SQL
            var escapedSql = EscapeStringLiteral(orderByInfo.ColumnSql);
            var directionSuffix = orderByInfo.IsDescending ? " DESC" : " ASC";
            var builderMethod = isOrderBy ? "AddOrderByClause" : "AddThenByClause";
            sb.AppendLine($"        return {returnCastOpen}{builderVar}.{builderMethod}(@\"{escapedSql}\", direction){bitSuffix}{returnCastClose};");
        }
        else if (clauseInfo != null && clauseInfo.IsSuccess)
        {
            // Non-OrderByClauseInfo but still successful
            var escapedSql = EscapeStringLiteral(clauseInfo.SqlFragment);
            var builderMethod = isOrderBy ? "AddOrderByClause" : "AddThenByClause";
            sb.AppendLine($"        return {returnCastOpen}{builderVar}.{builderMethod}(@\"{escapedSql}\", direction){bitSuffix}{returnCastClose};");
        }
        else
        {
            // Non-translatable clause — skip interceptor entirely so the original method runs
            return;
        }

        sb.AppendLine($"    }}");
    }

